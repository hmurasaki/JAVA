package ex;

public class Teacher {
	
	private int year;
	private int age;
	
	public Teacher() {
	}
	
	public Teacher(int year, int age) {
		super();
		this.year = year;
		this.age = age;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
	
	
}
